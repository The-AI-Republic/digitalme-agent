#!/usr/bin/env node

/**
 * Reads all .md template files from src/prompts/templates/ and generates
 * a TypeScript module that exports them as a Record<string, string>.
 *
 * Run before tsc so the compiled output has no runtime fs dependency
 * for prompt templates.
 */

import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const templatesDir = join(__dirname, '..', 'src', 'prompts', 'templates');
const outFile = join(__dirname, '..', 'src', 'prompts', 'templates.generated.ts');

const files = readdirSync(templatesDir).filter((f) => f.endsWith('.md')).sort();

const entries = files.map((file) => {
  const name = file.replace(/\.md$/, '');
  const content = readFileSync(join(templatesDir, file), 'utf-8');
  return `  ${JSON.stringify(name)}: ${JSON.stringify(content)},`;
});

const output = `// Auto-generated by scripts/embed-templates.js — do not edit manually.

export const EMBEDDED_TEMPLATES: Record<string, string> = {
${entries.join('\n')}
};
`;

writeFileSync(outFile, output, 'utf-8');
console.log(`Embedded ${files.length} templates into ${outFile}`);
